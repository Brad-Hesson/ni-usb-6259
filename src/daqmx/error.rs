pub trait ErrorValue {
    fn check(self) -> Result<(), Error>;
}
impl ErrorValue for i32 {
    fn check(self) -> Result<(), Error> {
        match self.cmp(&0) {
            std::cmp::Ordering::Less => return Err(Error::try_from(self).unwrap()),
            std::cmp::Ordering::Greater => {
                tracing::warn!("{:?}", Warning::try_from(self).unwrap())
            }
            _ => {}
        }
        Ok(())
    }
}

